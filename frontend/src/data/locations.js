// Helper function to search locations
export const searchLocations = (query, limit = 10) => {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  return locations
    .filter(location => 
      location.name.toLowerCase().includes(lowerQuery)
    )
    .slice(0, limit);
};

// Helper function to get location by coordinates
export const getLocationByCoordinates = (lat, lng, tolerance = 0.1) => {
  return locations.find(location => 
    Math.abs(location.lat - lat) < tolerance && 
    Math.abs(location.lng - lng) < tolerance
  );
};
